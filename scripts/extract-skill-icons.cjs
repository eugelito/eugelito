/**
 * Regenerates src/data/skillBrandIcons.js from simple-icons (devDependency).
 * Run: npm run extract-skill-icons
 */
const fs = require("fs");
const path = require("path");
const si = require("simple-icons");

const keys = [
  "siHtml5",
  "siCss",
  "siMarkdown",
  "siTailwindcss",
  "siDotnet",
  "siJavascript",
  "siAngular",
  "siReact",
  "siWordpress",
  "siGithub",
  "siFirebase",
  "siGithubcopilot",
  "siCursor",
];

const icons = {};
for (const k of keys) {
  if (!si[k]) throw new Error(`simple-icons: missing ${k}`);
  const { path: d, hex, title } = si[k];
  icons[k] = { path: d, hex, title };
}

const out = `/** Generated by scripts/extract-skill-icons.cjs — do not edit by hand */
export const skillBrandIcons = ${JSON.stringify(icons, null, 2)};
`;

const dest = path.join(__dirname, "..", "src", "data", "skillBrandIcons.js");
fs.mkdirSync(path.dirname(dest), { recursive: true });
fs.writeFileSync(dest, out);
console.log("Wrote", dest);
